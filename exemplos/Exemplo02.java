package exemplos;

public class Exemplo02 {
    
    public static void main(String[] args) {
        // TIPO NOME;
        int numero; // cria uma variável chamada número, que guarda um valor inteiro

        numero = 12; // atribuição = coloca um valor na variável

        System.out.println(numero);

        numero = 55;

        System.out.println(numero);

        System.out.println(numero + 10); // não altera a variável

        numero = numero + 10; // altera o valor da variável
         
        System.out.println(numero);
    }

}
